"use strict";
// Função para multiplicar dois números
function multiply(num1, num2) {
    return num1 * num2;
}
// Função para retornar uma saudação
function greet(name) {
    return `Olá ${name}`;
}
const product = multiply(4, 5); // Resultado = 20
console.log(product);
const greeting = greet("Mundo"); // Resultado = "Olá Mundo"
console.log(greeting);
